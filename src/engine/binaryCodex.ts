/// Decode binary string (space-separated bytes) to UTF-8 text
export function decodeBinary(binary: string): string {
  return binary
    .trim()
    .split(/\s+/)
    .map((byte) => String.fromCharCode(parseInt(byte, 2)))
    .join('');
}

/// Encode UTF-8 text to binary string (space-separated bytes)
export function encodeBinary(text: string): string {
  return Array.from(text)
    .map((char) => char.charCodeAt(0).toString(2).padStart(8, '0'))
    .join(' ');
}

